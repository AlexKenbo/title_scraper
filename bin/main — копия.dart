  Future<Map<String, dynamic>> _fetchSerp(String domain) async { 
    try {
      http.Response response = await http.get(
        'http://$domain');

      if (response.statusCode != 200 && response.statusCode != 201) {
        return {'error': domain};
      } else {

        //print(response.statusCode); 
        //print(response.body); 
        var document = parse(response.body);
        Element title = document.querySelector('head > title');
        Element meta = document.querySelector("head > meta[name='description']");
        
      // print(title.text);
      // print(meta.attributes['content']);
        String desc = 'NO';
        if(meta != null && meta.attributes.containsKey('content')){
          desc = meta.attributes['content'];  
        } 

        final Map<String, dynamic> responseData = {
          'data': domain, 
          'title': title?.text?? 'no',
          'desc': desc
          };
        //print(responseData); 
        return responseData;
      }
    } catch (e)  {
      print('$domain ${e.toString()}');
      final Map<String, dynamic> responseData = {
        'data': domain, 
        'title': e.toString(),
        'desc': 'NO'
        };
      //print(responseData); 
      return responseData;
    } 
  }

